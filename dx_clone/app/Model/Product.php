	public function get_product_by_category($id)
	{
		$products = $this->find('all',
			array(
				'conditions' => array('category_id' => $id),
				'recursive' => -1,
				'limit' => 10,
				'fields' => array('name', 'price', 'thumbnail', 'description', 'id', 'category_id', 'slug'),
				'contain' => array(
					'Category' => array(
						'fields' => array('id', 'name', 'slug')
					)
				)
			)
		);
		return $products;
	}

